class BookDTO 
    def title=(titleArg)
        @title = titleArg
    end
    def title
        @title
    end

    def author=(authorArg)
        @author = authorArg
    end
    def author
        @author
    end

    def year=(yearArg)
        @year = yearArg
    end
    def year
        @year
    end

    def pages=(pagesArg)
        @pages = pagesArg
    end
    def pages
        @pages
    end

    def reviews=(reviewsArg)
        @reviews = reviewsArg
    end
    def reviews
        @reviews
    end
end